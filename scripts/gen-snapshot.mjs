// Generates web/lib/snapshot.ts — an on-chain-derived activity snapshot (per-market
// volume + recent trades) read by the frontend at runtime. Needed because X Layer
// testnet RPCs cap eth_getLogs to a 100-block range, so the app cannot scan history
// live; prices stay live via eth_call, only volume/ticker/leaderboard use this snapshot.
// Scans FPMMBuy/FPMMSell in 100-block chunks over a bounded recent window.
//
//   RPC_URL=… CHAIN_ID=1952 FACTORY=0x… node gen-snapshot.mjs
// Optional: SNAPSHOT_LOOKBACK_BLOCKS (default 14000), SNAPSHOT_MAX_TRADES (default 120)
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { parseAbiItem, formatUnits } from 'viem';
import { getPublicClient, envAddress } from './lib/chain.mjs';
import { factoryAbi } from './lib/abis.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const DEC = 6;
const CHUNK = 100n; // RPC caps eth_getLogs block range at 100
const LOOKBACK = BigInt(process.env.SNAPSHOT_LOOKBACK_BLOCKS || 14000);
const MAX_TRADES = Number(process.env.SNAPSHOT_MAX_TRADES || 120);

const BUY = parseAbiItem('event FPMMBuy(address indexed buyer, uint8 outcomeIndex, uint256 investmentAmount, uint256 feeAmount, uint256 outcomeTokensBought)');
const SELL = parseAbiItem('event FPMMSell(address indexed seller, uint8 outcomeIndex, uint256 returnAmount, uint256 feeAmount, uint256 outcomeTokensSold)');

async function main() {
  const factory = envAddress('FACTORY');
  const client = getPublicClient();

  const len = Number(await client.readContract({ address: factory, abi: factoryAbi, functionName: 'marketsLength' }));
  const addresses = [];
  for (let i = 0; i < len; i++) {
    addresses.push(await client.readContract({ address: factory, abi: factoryAbi, functionName: 'allMarkets', args: [BigInt(i)] }));
  }

  const latest = await client.getBlockNumber();
  const from = latest > LOOKBACK ? latest - LOOKBACK : 0n;
  console.log(`Factory ${factory}: ${len} markets. Scanning blocks ${from}..${latest} in ${CHUNK}-block chunks.`);

  const trades = [];
  const volumeByMarket = {};
  for (let lo = from; lo <= latest; lo += CHUNK + 1n) {
    const hi = lo + CHUNK > latest ? latest : lo + CHUNK;
    let buys = [], sells = [];
    try {
      [buys, sells] = await Promise.all([
        client.getLogs({ address: addresses, event: BUY, fromBlock: lo, toBlock: hi }),
        client.getLogs({ address: addresses, event: SELL, fromBlock: lo, toBlock: hi }),
      ]);
    } catch (e) {
      console.log(`  chunk ${lo}..${hi} skipped: ${e.shortMessage || e.message}`);
      continue;
    }
    for (const l of buys) {
      const market = l.address.toLowerCase();
      const amount = Number(formatUnits(l.args.investmentAmount, DEC));
      volumeByMarket[market] = (volumeByMarket[market] || 0) + amount;
      trades.push({ side: 'buy', market, trader: l.args.buyer.toLowerCase(), outcomeIndex: Number(l.args.outcomeIndex), amount, shares: Number(formatUnits(l.args.outcomeTokensBought, DEC)), txHash: l.transactionHash, blockNumber: l.blockNumber });
    }
    for (const l of sells) {
      const market = l.address.toLowerCase();
      const amount = Number(formatUnits(l.args.returnAmount, DEC));
      volumeByMarket[market] = (volumeByMarket[market] || 0) + amount;
      trades.push({ side: 'sell', market, trader: l.args.seller.toLowerCase(), outcomeIndex: Number(l.args.outcomeIndex), amount, shares: Number(formatUnits(l.args.outcomeTokensSold, DEC)), txHash: l.transactionHash, blockNumber: l.blockNumber });
    }
  }

  trades.sort((a, b) => Number(b.blockNumber - a.blockNumber));
  const recent = trades.slice(0, MAX_TRADES);
  const totalVolume = Object.values(volumeByMarket).reduce((a, b) => a + b, 0);
  console.log(`Found ${trades.length} trades, total volume $${totalVolume.toFixed(2)} across ${Object.keys(volumeByMarket).length} markets. Bundling ${recent.length}.`);

  const round = (n) => Math.round(n * 100) / 100;
  const tradeLines = recent
    .map((t) => `  { side: "${t.side}", market: "${t.market}", trader: "${t.trader}", outcomeIndex: ${t.outcomeIndex}, amount: ${round(t.amount)}, shares: ${round(t.shares)}, txHash: "${t.txHash}", blockNumber: ${t.blockNumber}n },`)
    .join('\n');
  const volLines = Object.entries(volumeByMarket)
    .map(([m, v]) => `    "${m}": ${round(v)},`)
    .join('\n');

  const out = `// AUTO-GENERATED by scripts/gen-snapshot.mjs — do not edit by hand.
// On-chain-derived activity snapshot read at runtime. X Layer testnet RPCs cap
// eth_getLogs to a 100-block range, so the app cannot scan trade history live;
// prices stay live via eth_call, volume/ticker/leaderboard read this snapshot.
export interface SnapshotTrade {
  side: "buy" | "sell";
  market: \`0x\${string}\`;
  trader: \`0x\${string}\`;
  outcomeIndex: number;
  amount: number;
  shares: number;
  txHash: \`0x\${string}\`;
  blockNumber: bigint;
}

export interface ActivitySnapshot {
  generatedAt: string;
  totalVolume: number;
  volumeByMarket: Record<string, number>;
  trades: SnapshotTrade[];
}

export const SNAPSHOT: ActivitySnapshot = {
  generatedAt: "${new Date().toISOString()}",
  totalVolume: ${round(totalVolume)},
  volumeByMarket: {
${volLines}
  },
  trades: [
${tradeLines}
  ],
};
`;

  const dest = resolve(here, '..', 'web', 'lib', 'snapshot.ts');
  writeFileSync(dest, out);
  console.log(`Wrote ${dest}`);
}

main().catch((e) => { console.error('gen-snapshot failed:', e.shortMessage || e.message); process.exitCode = 1; });
